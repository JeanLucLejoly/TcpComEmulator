    private void CheckAndHandleAutoAck(String str2Check,String ackId, String ackMsg ){
        if (!str2Check.contentEquals(".")){
            if (ackId.length()>0){
                if (ackMsg.length()>0){
                    if (str2Check.contains(ackId)){
                        String text2Put;
                        NowConection.SendData(ackMsg);
                        text2Put = NowConection.getOutput();
                        jTextArea2.setText(text2Put);
                        LogData = LogData + text2Put + "\n\r";
                        jTextArea3.setText(LogData);
                    }
                }
            }
        }
        // test what goes on here
        return;
    }
    
