    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO for sending MessageQueue, next message:
        // first we take content of field into a string, and make it a JSON format
        String MessageString = jTextArea4.getText();
        // remove linefeed
        MessageString = MessageString.replace("\r\n", "\n");
        MessageString = MessageString.replace("\n\r", "\n");
        String[] MessageArray = MessageString.split("\n");
        // get current counter 
        int NowMsgNb = Integer.parseInt(jTextField2.getText());
        jTextArea1.setText(MessageArray[NowMsgNb]);
        SendTextArea1AndRead();
        NowMsgNb++;
        jTextField2.setText(Integer.toString(NowMsgNb));

    }//GEN-LAST:event_jButton4ActionPerformed

