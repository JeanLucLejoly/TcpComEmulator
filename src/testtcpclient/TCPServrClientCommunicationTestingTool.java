    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO for connect button:
        if (connectStarted){
            // dont do anything
        }else{
        String text2Put="";
        String PortNbStr = jTextField5.getText();
        int portNb = Integer.parseInt(PortNbStr);
        String cnxType=jComboBox1.getSelectedItem().toString();
        
        if (cnxType.contains("CLIENT")){
            NowConection = new TestTCPClient(jTextField1.getText(), portNb);
            text2Put = NowConection.getOutput();
            setBackground(new java.awt.Color(199, 236, 240));
            jPanel3.setBackground(new java.awt.Color(255, 254, 217));        
            if (text2Put.contains("failed")){
                connectStarted=false;
            }else{
                connectStarted=true;
            }
        }
        if (cnxType.contains("SERVER")){
        try {
            NowConection = new TestTCPClient(portNb);
            connectStarted=true;
            } catch (IOException ex) {
                Logger.getLogger(TCPServrClientCommunicationTestingTool.class.getName()).log(Level.SEVERE, null, ex);
                connectStarted=false;
            }
            text2Put = NowConection.getOutput();
            setBackground(new java.awt.Color(255, 254, 217));
            jPanel3.setBackground(new java.awt.Color(199, 236, 240));

            //jPanel3.setBackground(Color.YELLOW);
        }
        jTextArea2.setText(text2Put);
        LogData = LogData + text2Put + "\n\r";
        jTextArea3.setText(LogData);
        if (connectStarted){
            jComboBox1.setEnabled(false);
            jButton1.setEnabled(false);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jButton20.setEnabled(true);
            jButton22.setEnabled(true);
            jButton23.setEnabled(true);
            jButton4.setEnabled(true);
            jToggleButton1.setEnabled(true);
        }else{
            jComboBox1.setEnabled(true);
            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton20.setEnabled(false);
            jButton22.setEnabled(false);
            jButton23.setEnabled(false);
            jButton4.setEnabled(false);
            jToggleButton1.setEnabled(false);
        }
            
        }
        
         
    }//GEN-LAST:event_jButton1ActionPerformed

